void	ft_fill_str(char *buffer, int size)
{
	int count;

	count = 0;
	while (count < size)
	{
		buffer[count] = '\0';
		count++;
	}
}

void	ft_swap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}